<template>
  <div class="tab-control">
      <div v-for="(item,index) in titles" :key='item.index'  class="tab-control-item" @click="itemClick(index)" :class="{active:index=== currentIndex}"><span >{{item}}</span></div>
  </div>
</template>

<script>
export default {
  props:{
      titles:{
          type:Array,
          default(){
              return []
          }
      }
  },
  data(){
      return{
          currentIndex:0
      }
  },
  methods:{
      itemClick(index){
          this.currentIndex = index
      }
  }
}
</script>

